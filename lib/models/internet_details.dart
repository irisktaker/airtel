import 'package:flutter/cupertino.dart';

class InternetDetails {
  String icon;
  String text;
  double value;
  String indicator;
  Color bgColor;
  Color borderColor;
  Color foregroundColor;


  InternetDetails({
    required this.icon,
    required this.text,
    required this.value,
    required this.indicator,
    required this.bgColor,
    required this.borderColor,
    required this.foregroundColor,
  });
}